package mavenCrawler.mavenCrawler;

public class Page {

	private StringBuilder title;
	private StringBuilder content;
	private StringBuilder mp3;
	private StringBuilder date;
	
	public StringBuilder getTitle() {
		return title;
	}
	public void setTitle(StringBuilder title) {
		this.title = title;
	}
	public StringBuilder getContent() {
		return content;
	}
	public void setContent(StringBuilder content) {
		this.content = content;
	}
	public StringBuilder getMp3() {
		return mp3;
	}
	public void setMp3(StringBuilder mp3) {
		this.mp3 = mp3;
	}
	public StringBuilder getDate() {
		return date;
	}
	public void setDate(StringBuilder date) {
		this.date = date;
	}
	
	
}
